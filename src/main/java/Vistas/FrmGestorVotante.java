    private void botonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarActionPerformed

        String documento = campoDocumento.getText();
        String nombre = campoNombre.getText();
        String telefono = campoTelefono.getText();
        String correo = campoCorreo.getText();
        String ciudad = comboCiudadOrigen.getSelectedItem().toString();
        String direccion = campoDireccion.getText();

        ClsVotante votante = new ClsVotante(ciudad, direccion, documento, nombre, telefono, correo);

        ClsMensaje mensaje = this.controlador.agregar(votante);

        if (mensaje.getTipo().equals(ClsMensaje.OK)) {
            ObtenerDatos();
        }

        JOptionPane.showMessageDialog(rootPane, mensaje.getTexto());


    }//GEN-LAST:event_botonAgregarActionPerformed

