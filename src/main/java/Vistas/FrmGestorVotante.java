    private void ActualizarTabla(LinkedList<Object> lista) {

        DefaultTableModel modelo = (DefaultTableModel) tablaCandidatos.getModel();
        modelo.setRowCount(0);

        for (Object objeto : lista) {
            
            ClsVotante v = (ClsVotante) objeto;

            Object[] fila = {v.getNumeroDocumento(), v.getNombre(),
                v.getDireccion(),};

            modelo.addRow(fila);

        }

    }

